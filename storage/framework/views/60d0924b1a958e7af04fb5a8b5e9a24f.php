    $('.delete-category').click(function () {

        const id = $(this).data('cat-id');
        let url = "<?php echo e(route('discussion-category.destroy', ':id')); ?>";
        url = url.replace(':id', id);

        const token = "<?php echo e(csrf_token()); ?>";

        Swal.fire({
            title: "<?php echo app('translator')->get('messages.sweetAlertTitle'); ?>",
            text: "<?php echo app('translator')->get('messages.recoverRecord'); ?>",
            icon: 'warning',
            showCancelButton: true,
            focusConfirm: false,
            confirmButtonText: "<?php echo app('translator')->get('messages.confirmDelete'); ?>",
            cancelButtonText: "<?php echo app('translator')->get('app.cancel'); ?>",
            customClass: {
                confirmButton: 'btn btn-primary mr-3',
                cancelButton: 'btn btn-secondary'
            },
            showClass: {
                popup: 'swal2-noanimation',
                backdrop: 'swal2-noanimation'
            },
            buttonsStyling: false
        }).then((result) => {
            if (result.isConfirmed) {
                $.easyAjax({
                    type: 'POST',
                    url: url,
                    data: {
                        '_token': token,
                        '_method': 'DELETE'
                    },
                    success: function (response) {
                        if (response.status === 'success') {
                            $('#discussion_category').html(response.data);
                            $('#discussion_category').selectpicker('refresh');
                            showTable();
                            $(MODAL_LG).modal('hide');
                        }
                    }
                });
            }
        });

    });

    $('#save-category').click(function () {
        const url = "<?php echo e(route('discussion-category.store')); ?>";
        $.easyAjax({
            url: url,
            container: '#createTaskCategory',
            disableButton: true,
            blockUI: true,
            buttonSelector: "#save-category",
            type: "POST",
            data: $('#createTaskCategory').serialize(),
            success: function (response) {
                if (response.status == 'success') {
                    $('#discussion_category').html(response.data);
                    $('#discussion_category').selectpicker('refresh');
                    $(MODAL_LG).modal('hide');
                }
            }
        })
    });

    $('[contenteditable=true]').focus(function () {
        $(this).data("initialText", $(this).html());
    }).blur(function () {
        if ($(this).data("initialText") !== $(this).html()) {
            let id = $(this).data('row-id');
            let value = $(this).html();

            let url = "<?php echo e(route('discussion-category.update', ':id')); ?>";
            url = url.replace(':id', id);

            const token = "<?php echo e(csrf_token()); ?>";

            $.easyAjax({
                url: url,
                container: '#row-' + id,
                type: "POST",
                data: {
                    'name': value,
                    '_token': token,
                    '_method': 'PUT'
                },
                blockUI: true,
                success: function (response) {
                    if (response.status === 'success') {
                        $('#discussion_category').html(response.data);
                        $('#discussion_category').selectpicker('refresh');
                    }
                }
            })
        }
    });

