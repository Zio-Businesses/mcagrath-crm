        function showTable() {
            const dateRangePicker = $('#datatableRange').data('daterangepicker');

            let startDate = $('#datatableRange').val();
            let endDate;

            if (startDate === '') {
                startDate = null;
                endDate = null;
            } else {
                startDate = dateRangePicker.startDate.format('<?php echo e(company()->moment_date_format); ?>');
                endDate = dateRangePicker.endDate.format('<?php echo e(company()->moment_date_format); ?>');
                $('#start-date').val(startDate);
                $('#end-date').val(endDate);
            }

            var projectID = $('#project_id').val();
            var employee = $('#employee').val();

            var token = "<?php echo e(csrf_token()); ?>";
            var url = "<?php echo e(route('timelogs.employee_data')); ?>";

            $.easyAjax({
                type: 'POST',
                url: url,
                data: {
                    '_token': token,
                    startDate: startDate,
                    endDate: endDate,
                    projectID: projectID,
                    employee: employee
                },
                success: function(response) {
                    $('#member-list').html(response.html);
                }
            });
        }

