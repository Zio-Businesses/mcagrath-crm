        var url = "<?php echo e(route('gdpr.edit_consent', ':id')); ?>";
        url = url.replace(':id', consentID);

        $(MODAL_LG + ' ' + MODAL_HEADING).html('...');
        $.ajaxModal(MODAL_LG, url);
    })
