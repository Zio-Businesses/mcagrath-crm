    $('#save-unit').click(function() {
        var url = "<?php echo e(route('unit-type.store')); ?>";
        $.easyAjax({
            url: url,
            container: '#createUnitType',
            type: "POST",
            data: $('#createUnitType').serialize(),
            success: function(response) {
                if (response.status == 'success') {
                    $('#unit_type_id').html(response.data);
                    $('#unit_type_id').selectpicker('refresh');
                    $(MODAL_LG).modal('hide');
                    window.location.reload();
                }
            }
        })
    });

    $('[contenteditable=true]').focus(function() {
        $(this).data("initialText", $(this).html());
        let rowId = $(this).data('row-id');
    }).blur(function() {
        if ($(this).data("initialText") !== $(this).html()) {
            let id = $(this).data('row-id');
            let value = $(this).html();

            var url = "<?php echo e(route('unit-type.update', ':id')); ?>";
            url = url.replace(':id', id);

            var token = "<?php echo e(csrf_token()); ?>";

            $.easyAjax({
                url: url,
                container: '#row-' + id,
                type: "POST",
                data: {
                    'unit_type': value,
                    '_token': token,
                    '_method': 'PUT'
                },
                blockUI: true,
                success: function(response) {
                    if (response.status == 'success') {
                        $('#unit_type_id').html(response.data);
                        $('#unit_type_id').selectpicker('refresh');
                    }
                }
            })
        }
    });

</script>

